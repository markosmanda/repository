public class Persona {
      protected int ci;
    protected String nombre;

    public Persona(int ci, String nombre) {
        this.ci = ci;
        this.nombre = nombre;
    }

    public int getCi() {
        return ci;
    }

    public void setCi(int ci) {
        this.ci = ci;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}


